package org.yexiaonan;

public class ForContinue {
    public static void main(String[] args) {
        for (int i = 1; i<=5;i++){
            if (i == 3){
                continue;
            }
            System.out.println("木棍吃了" + i + "个木棍");
        }

        for (int i = 1; i<=5;i++){
            if (i == 3){
                continue;
            }
            System.out.println("木棍吃了" + i + "个木棍");
        }
    }
}
